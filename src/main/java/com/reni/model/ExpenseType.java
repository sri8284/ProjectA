package com.reni.model;

public class ExpenseType extends Basic {

	private String expType;
	private String expSubType;
	private String expTypeName;
	private String expSubTypeName;
	public String getExpType() {
		return expType;
	}
	public void setExpType(String expType) {
		this.expType = expType;
	}
	public String getExpSubType() {
		return expSubType;
	}
	public void setExpSubType(String expSubType) {
		this.expSubType = expSubType;
	}
	public String getExpTypeName() {
		return expTypeName;
	}
	public void setExpTypeName(String expTypeName) {
		this.expTypeName = expTypeName;
	}
	public String getExpSubTypeName() {
		return expSubTypeName;
	}
	public void setExpSubTypeName(String expSubTypeName) {
		this.expSubTypeName = expSubTypeName;
	}
	
	
}
